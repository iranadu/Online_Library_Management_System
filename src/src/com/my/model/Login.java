package com.my.model;

public class Login {

		private String uType;
		private String uid;
		private String pass;
		
		
		public void setType(String uType){
			this.uType = uType;
		}
		public String getType(){
			return uType;
		}
		
		
		public void setUid(String uid){
			this.uid = uid;
		}
		public String getUid(){
			return uid;
		}
		
		public void setPass(String pass){
			this.pass = pass;
		}
		public String getPass(){
			return pass;
		}
	
}
